class Solution {
    public int numsum(int n){
        int sum=0;
        while(n>0){
            sum+=n%10;
            n/=10;
        }
        return sum;
    }
    public int smallestIndex(int[] nums) {
        int n=nums.length;
        for(int i=0;i<n;i++){
            if(i==numsum(nums[i])){
                return i;
            }
        }
        return -1;
    }
}